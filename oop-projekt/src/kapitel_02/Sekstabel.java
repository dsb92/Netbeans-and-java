package kapitel_02;

//Sekstabel ved brug af Løkker og while-løkken

public class Sekstabel
{
	public static void main(String[] arg) 
	{
	   
            int n;
            n = 1;
            
            while (n<=10)
            {
                System.out.println(n+":"+6*n);
                n = n+1;
            }    
                   
 
	}
}
